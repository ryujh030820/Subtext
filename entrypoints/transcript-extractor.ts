    function extractParams(data: unknown): string | null {
      if (!isObject(data)) return null;
      const panels = (data as AnyObj).engagementPanels;
      if (!Array.isArray(panels)) return null;
      for (const p of panels) {
        if (!isObject(p)) continue;
        const r = (p as AnyObj).engagementPanelSectionListRenderer;
        if (!isObject(r) || (r as AnyObj).targetId !== 'engagement-panel-searchable-transcript') continue;
        const params = (r as AnyObj).content?.continuationItemRenderer?.continuationEndpoint?.getTranscriptEndpoint?.params;
        if (typeof params === 'string' && params.length > 0) return params;
      }
      return null;
    }

