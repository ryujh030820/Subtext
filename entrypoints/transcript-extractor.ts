    function scrapeTranscriptFromDom(): TranscriptSegment[] {
      // YouTube의 이미 렌더링된 transcript 패널에서 세그먼트 읽기
      const segmentEls = document.querySelectorAll('ytd-transcript-segment-renderer, div.segment.style-scope.ytd-transcript-segment-renderer');
      if (segmentEls.length === 0) return [];

      const segments: TranscriptSegment[] = [];
      for (const el of Array.from(segmentEls)) {
        const timestampEl = el.querySelector('.segment-timestamp, .cue-group-start-offset, #start-offset');
        const textEl = el.querySelector('.segment-text, #segment-text, yt-formatted-string');
        if (!timestampEl || !textEl) continue;

        const text = (textEl.textContent ?? '').trim();
        if (!text) continue;

        const timeStr = (timestampEl.textContent ?? '').trim();
        const offset = parseTimestamp(timeStr);
        segments.push({ text, offset, duration: 0, lang: '' });
      }
      return normalizeSegments(segments);
    }

    function parseTimestamp(ts: string): number {
      const parts = ts.split(':').map(Number);
      if (parts.length === 3) return parts[0] * 3600 + parts[1] * 60 + parts[2];
      if (parts.length === 2) return parts[0] * 60 + parts[1];
      return parts[0] || 0;
    }

