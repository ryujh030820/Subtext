    function readTrackLanguageCode(value: AnyObj): string {
      const candidates = [
        value.languageCode,
        value.language_code,
        value.lang,
      ];
      for (const candidate of candidates) {
        if (typeof candidate === 'string' && candidate.trim()) {
          return candidate.trim();
        }
      }
      return '';
    }

    function normalizeLanguageCodeForCompare(code: string): string {
      const normalized = code.replace(/_/g, '-').toLowerCase();
      if (normalized.startsWith('zh-hans')) return 'zh-hans';
      if (normalized.startsWith('zh-hant')) return 'zh-hant';
      return normalized.split('-')[0] ?? normalized;
    }

    function findBestTranslationLanguage(targetLanguage: string, candidates: AnyObj[]): AnyObj | null {
      if (candidates.length === 0) {
        return { languageCode: targetLanguage };
      }

      const normalizedTarget = normalizeLanguageCodeForCompare(targetLanguage);
      let exact: AnyObj | null = null;
      let baseMatch: AnyObj | null = null;

      for (const candidate of candidates) {
        const code = readTrackLanguageCode(candidate);
        if (!code) continue;
        if (code.toLowerCase() === targetLanguage.toLowerCase()) {
          exact = candidate;
          break;
        }
        if (normalizeLanguageCodeForCompare(code) === normalizedTarget) {
          baseMatch = candidate;
        }
      }

      return exact ?? baseMatch;
    }

