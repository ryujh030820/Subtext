    function rankTracksForTranslation(tracks: AnyObj[]): AnyObj[] {
      const preferredLanguages = ['en', 'ko', 'ja', 'es', 'fr', 'de', 'pt', 'it'];

      const getScore = (track: AnyObj): number => {
        const isAsr = track.kind === 'asr';
        const languageCode = typeof track.languageCode === 'string' ? track.languageCode.toLowerCase() : '';
        const baseUrl = typeof track.baseUrl === 'string' ? track.baseUrl : '';
        const isTranslatable = track.isTranslatable !== false;
        let score = 0;

        if (isTranslatable) score += 50;
        if (!isAsr) score += 20;

        const preferredIndex = preferredLanguages.indexOf(languageCode);
        if (preferredIndex !== -1) {
          score += (preferredLanguages.length - preferredIndex);
        }

        if (baseUrl.includes('lang=')) score += 5;
        if (baseUrl.includes('vss_id=')) score += 3;
        if (baseUrl.includes('/api/timedtext')) score += 2;

        return score;
      };

      return [...tracks].sort((a, b) => getScore(b) - getScore(a));
    }

