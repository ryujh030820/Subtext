    function parseJson3Captions(data: AnyObj, lang: string): TranscriptSegment[] {
      const events = data.events as AnyObj[] | undefined;
      if (!Array.isArray(events)) return [];

      const segments: TranscriptSegment[] = [];
      for (const ev of events) {
        const segs = ev.segs as AnyObj[] | undefined;
        if (!Array.isArray(segs)) continue;
        const text = segs.map(s => (s.utf8 as string) || '').join('').trim();
        if (!text || text === '\n') continue;
        const tStartMs = Number(ev.tStartMs);
        const dDurationMs = Number(ev.dDurationMs);
        if (!Number.isFinite(tStartMs)) continue;
        segments.push({
          text,
          offset: tStartMs / 1000,
          duration: Number.isFinite(dDurationMs) ? dDurationMs / 1000 : 0,
          lang,
        });
      }
      return normalizeSegments(segments);
    }

    function parseXmlCaptions(xml: string, lang: string): TranscriptSegment[] {
      const parser = new DOMParser();
      const doc = parser.parseFromString(xml, 'text/xml');
      const textElements = doc.querySelectorAll('text');
      const segments: TranscriptSegment[] = [];

      for (const el of Array.from(textElements)) {
        const text = (el.textContent ?? '').replace(/&amp;/g, '&').replace(/&lt;/g, '<').replace(/&gt;/g, '>').replace(/&#39;/g, "'").replace(/&quot;/g, '"').trim();
        if (!text) continue;
        const start = parseFloat(el.getAttribute('start') ?? '0');
        const dur = parseFloat(el.getAttribute('dur') ?? '0');
        segments.push({ text, offset: start, duration: dur, lang });
      }
      return normalizeSegments(segments);
    }

    function parseVttCaptions(vtt: string, lang: string): TranscriptSegment[] {
      const segments: TranscriptSegment[] = [];
      const blocks = vtt.split(/\n\n+/);

      for (const block of blocks) {
        const lines = block.trim().split('\n');
        const timeLine = lines.find((line) => line.includes('-->'));
        if (!timeLine) continue;

        const [startStr, endStr] = timeLine.split('-->').map((value) => value.trim());
        const start = parseVttTime(startStr);
        const end = parseVttTime(endStr);
        const text = lines
          .slice(lines.indexOf(timeLine) + 1)
          .join(' ')
          .replace(/<[^>]+>/g, '')
          .trim();

        if (!text) continue;
        segments.push({
          text,
          offset: start,
          duration: end > start ? end - start : 0,
          lang,
        });
      }

      return normalizeSegments(segments);
    }

    function parseVttTime(value: string): number {
      const clean = value.split(/\s+/)[0];
      const parts = clean.split(':');
      if (parts.length === 3) {
        return parseFloat(parts[0]) * 3600 + parseFloat(parts[1]) * 60 + parseFloat(parts[2].replace(',', '.'));
      }
      if (parts.length === 2) {
        return parseFloat(parts[0]) * 60 + parseFloat(parts[1].replace(',', '.'));
      }
      return parseFloat(clean.replace(',', '.')) || 0;
    }

