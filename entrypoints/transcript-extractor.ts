    function makeCaptionUrlCandidates(baseUrl: string): string[] {
      const out = new Set<string>();
      out.add(baseUrl);

      try {
        const u = new URL(baseUrl, location.origin);
        if (!u.searchParams.get('fmt')) {
          const json3 = new URL(u.toString());
          json3.searchParams.set('fmt', 'json3');
          out.add(json3.toString());

          const srv3 = new URL(u.toString());
          srv3.searchParams.set('fmt', 'srv3');
          out.add(srv3.toString());

          const vtt = new URL(u.toString());
          vtt.searchParams.set('fmt', 'vtt');
          out.add(vtt.toString());
        }
      } catch {
        if (!baseUrl.includes('fmt=')) {
          out.add(`${baseUrl}&fmt=json3`);
          out.add(`${baseUrl}&fmt=srv3`);
          out.add(`${baseUrl}&fmt=vtt`);
        }
      }

      return Array.from(out);
    }

    function makeTranslatedCaptionUrlCandidates(
      baseUrl: string,
      params: {
        sourceLang: string;
        trackKind: string;
        trackVssId: string;
        targetLang: string;
      },
    ): string[] {
      try {
        const url = new URL(baseUrl, location.origin);
        url.searchParams.set('tlang', params.targetLang);

        if (!url.searchParams.has('lang') && params.sourceLang) {
          url.searchParams.set('lang', params.sourceLang);
        }
        if (!url.searchParams.has('kind') && params.trackKind === 'asr') {
          url.searchParams.set('kind', 'asr');
        }
        if (!url.searchParams.has('vss_id') && params.trackVssId) {
          url.searchParams.set('vss_id', params.trackVssId);
        }
        url.searchParams.delete('fmt');

        if (isLikelySignedCaptionUrl(url)) {
          return [url.toString()];
        }

        return makeCaptionUrlCandidates(url.toString());
      } catch {
        if (baseUrl.includes('?')) {
          return [`${baseUrl}&tlang=${encodeURIComponent(params.targetLang)}`];
        }
        return [`${baseUrl}?tlang=${encodeURIComponent(params.targetLang)}`];
      }
    }

    function isLikelySignedCaptionUrl(url: URL): boolean {
      return (
        url.searchParams.has('signature')
        || url.searchParams.has('sig')
        || url.searchParams.has('lsig')
        || url.searchParams.has('expire')
        || url.searchParams.has('sparams')
      );
    }

