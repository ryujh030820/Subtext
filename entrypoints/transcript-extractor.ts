    function findBestMatchingPlayerTrack(
      tracklist: AnyObj[],
      requested: { languageCode: string; isAutoGenerated: boolean; label: string; vssId: string },
    ): AnyObj | null {
      const normalizedLabel = normalizeTrackLabel(requested.label);
      const normalizedLanguage = requested.languageCode.trim().toLowerCase();
      const requestedKind = requested.isAutoGenerated ? 'asr' : '';

      const scoreTrack = (track: AnyObj) => {
        let score = 0;
        const languageCode = typeof track.languageCode === 'string' ? track.languageCode.trim().toLowerCase() : '';
        const kind = track.kind === 'asr' ? 'asr' : '';
        const vssId = typeof track.vss_id === 'string'
          ? track.vss_id
          : (typeof track.vssId === 'string' ? track.vssId : '');
        const label = normalizeTrackLabel(readCaptionTrackText(track.name));

        if (normalizedLanguage && languageCode === normalizedLanguage) score += 4;
        if (requestedKind === kind) score += 3;
        if (requested.vssId && vssId === requested.vssId) score += 6;
        if (normalizedLabel && label === normalizedLabel) score += 2;
        return score;
      };

      const ranked = tracklist
        .map((track) => ({ track, score: scoreTrack(track) }))
        .filter((entry) => entry.score > 0)
        .sort((a, b) => b.score - a.score);

      return ranked[0]?.track ?? null;
    }

    function readCaptionTrackText(value: unknown): string {
      if (!isObject(value)) return '';
      const simpleText = value.simpleText;
      if (typeof simpleText === 'string') {
        return simpleText.trim();
      }

      const runs = value.runs;
      if (Array.isArray(runs)) {
        return runs
          .map((run) => (isObject(run) && typeof run.text === 'string' ? run.text : ''))
          .join('')
          .trim();
      }

      return '';
    }

    function dedupeTrackOptions(tracks: Array<{
      id: string;
      label: string;
      languageCode: string;
      isAutoGenerated: boolean;
      fetchUrl: string;
      vssId?: string;
      playerTrackData?: AnyObj;
      source: 'youtube';
    }>) {
      const seen = new Set<string>();
      const out: typeof tracks = [];

      for (const track of tracks) {
        const key = [
          track.languageCode.trim().toLowerCase(),
          track.isAutoGenerated ? 'auto' : 'manual',
          normalizeTrackLabel(track.label),
        ].join('|');

        if (seen.has(key)) {
          const existing = out.find((item) => [
            item.languageCode.trim().toLowerCase(),
            item.isAutoGenerated ? 'auto' : 'manual',
            normalizeTrackLabel(item.label),
          ].join('|') === key);
          if (existing && !existing.playerTrackData && track.playerTrackData) {
            existing.playerTrackData = track.playerTrackData;
            existing.vssId = existing.vssId || track.vssId;
            existing.fetchUrl = existing.fetchUrl || track.fetchUrl;
          }
          continue;
        }

        seen.add(key);
        out.push(track);
      }

      return out;
    }

    function normalizeTrackLabel(label: string) {
      return label.trim().replace(/\s+/g, ' ').toLowerCase();
    }

