function createDownloadFilename(filename: string, track: DownloadTrack, format: DownloadFormat) {
  const suffix = [track.languageCode, track.isAutoGenerated ? 'auto' : '']
    .filter(Boolean)
    .map((value) => sanitizeFilenamePart(value))
    .filter(Boolean)
    .join('.');
  const extension = format === 'srt' ? 'srt' : 'txt';
  return suffix ? `${filename}.${suffix}.${extension}` : `${filename}.${extension}`;
}

function sanitizeFilenamePart(value: string) {
  return value.replace(/[\\/:*?"<>|\s]+/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');
}

function withTimeout<T>(promise: Promise<T>, timeoutMs: number, message: string): Promise<T> {
  return new Promise<T>((resolve, reject) => {
    const timer = window.setTimeout(() => {
      reject(new Error(message));
    }, timeoutMs);

    promise
      .then((value) => {
        window.clearTimeout(timer);
        resolve(value);
      })
      .catch((error: unknown) => {
        window.clearTimeout(timer);
        reject(error);
      });
  });
}
